def student_report(name, marks, grade="Not Assigned", school="ABC School"):
    print("\n--- STUDENT REPORT ---")
    print("Name:", name)
    print("Marks:", marks)
    print("Grade:", grade)
    print("School:", school)

student_report("Raj", 85)
student_report("Aman", 92, grade="A")
student_report(name="Sita", marks=78, school="Delhi Public School")
